export const BS_MONTHS = ["Baisakh","Jestha","Ashadh","Shrawan","Bhadra","Ashwin","Kartik","Mangsir","Poush","Magh","Falgun","Chaitra"];

// AD epoch: BS 2000/1/1 = AD 1943/4/14
export const BS_EPOCH_AD = new Date(1943, 3, 14); // month is 0-indexed

export function bsTotalDays(y, m, d) {
  let total = 0;
  for (let yr = 2000; yr < y; yr++) {
    const row = BS_MONTH_DAYS[yr];
    if (row) row.forEach(days => total += days);
  }
  const row = BS_MONTH_DAYS[y];
  if (row) for (let mi = 0; mi < m - 1; mi++) total += row[mi];
  return total + d - 1;
}

export function bsToAd(y, m, d) {
  const days = bsTotalDays(y, m, d);
  const ad = new Date(BS_EPOCH_AD);
  ad.setDate(ad.getDate() + days);
  return ad;
}

export function adToBs(date) {
  const diff = Math.floor((date - BS_EPOCH_AD) / 86400000);
  let remaining = diff;
  let y = 2000;
  while (true) {
    const row = BS_MONTH_DAYS[y];
    if (!row) break;
    const total = row.reduce((a,b)=>a+b,0);
    if (remaining < total) break;
    remaining -= total;
    y++;
  }
  const row = BS_MONTH_DAYS[y] || BS_MONTH_DAYS[2080];
  let m = 1;
  for (let mi = 0; mi < 12; mi++) {
    if (remaining < row[mi]) { m = mi+1; break; }
    remaining -= row[mi];
  }
  return { y, m, d: remaining + 1 };
}

export function formatBs(bsObj) {
  if (!bsObj || !bsObj.y) return "";
  return `${bsObj.y}-${String(bsObj.m).padStart(2,"0")}-${String(bsObj.d).padStart(2,"0")}`;
}

export function parseBsStr(str) {
  if (!str) return null;
  const parts = str.split("-");
  if (parts.length !== 3) return null;
  const y = parseInt(parts[0]), m = parseInt(parts[1]), d = parseInt(parts[2]);
  if (isNaN(y)||isNaN(m)||isNaN(d)) return null;
  return { y, m, d };
}
